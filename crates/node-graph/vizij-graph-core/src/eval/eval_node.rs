/// Evaluates split.
fn eval_split(
    params: &NodeParams,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let input = input_or_default(inputs, "in");
    let data = flatten_numeric(&input.value)
        .map(|f| f.data)
        .unwrap_or_default();
    let sizes = params.sizes.clone().unwrap_or_default();
    let sizes_usize: Vec<usize> = sizes.iter().map(|x| x.floor().max(0.0) as usize).collect();

    let total: usize = sizes_usize.iter().sum();

    let mut offset = 0usize;
    for (i, sz) in sizes_usize.iter().copied().enumerate() {
        let value_vec = if total == data.len() {
            let slice = data.get(offset..offset + sz).unwrap_or(&[]).to_vec();
            offset = offset.saturating_add(sz);
            slice
        } else {
            vec![f32::NAN; sz]
        };
        outputs.set_variadic("parts", i, PortValue::new(Value::Vector(value_vec)))?;
    }

    // If no sizes provided, emit a single empty part.
    if sizes_usize.is_empty() {
        outputs.set_variadic("parts", 0, PortValue::new(Value::Vector(Vec::new())))?;
    }

    Ok(())
}

/// Evaluates vector reducer.
fn eval_vector_reducer(
    kind: &NodeType,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let mut data = flatten_numeric(&input_or_default(inputs, "in").value)
        .map(|f| f.data)
        .unwrap_or_default();
    let result = match kind {
        NodeType::VectorMin => {
            if data.is_empty() {
                f32::NAN
            } else {
                data.iter().fold(f32::INFINITY, |acc, x| acc.min(*x))
            }
        }
        NodeType::VectorMax => {
            if data.is_empty() {
                f32::NAN
            } else {
                data.iter().fold(f32::NEG_INFINITY, |acc, x| acc.max(*x))
            }
        }
        NodeType::VectorMean => {
            if data.is_empty() {
                f32::NAN
            } else {
                data.iter().sum::<f32>() / (data.len() as f32)
            }
        }
        NodeType::VectorMedian => {
            if data.is_empty() {
                f32::NAN
            } else {
                data.sort_by(|a, b| a.partial_cmp(b).unwrap_or(std::cmp::Ordering::Equal));
                let n = data.len();
                if n % 2 == 1 {
                    data[n / 2]
                } else {
                    (data[n / 2 - 1] + data[n / 2]) / 2.0
                }
            }
        }
        NodeType::VectorMode => {
            if data.is_empty() {
                f32::NAN
            } else {
                let mut counts: hashbrown::HashMap<i64, (f32, usize)> = hashbrown::HashMap::new();
                for value in data {
                    if value.is_nan() {
                        continue;
                    }
                    let key = value.to_bits() as i64;
                    let entry = counts.entry(key).or_insert((value, 0));
                    entry.1 += 1;
                }
                if counts.is_empty() {
                    f32::NAN
                } else {
                    let mut best_val = f32::NAN;
                    let mut best_count = 0usize;
                    for (_key, (val, cnt)) in counts.iter() {
                        if *cnt > best_count {
                            best_count = *cnt;
                            best_val = *val;
                        } else if *cnt == best_count && val < &best_val {
                            best_val = *val;
                        }
                    }
                    best_val
                }
            }
        }
        _ => f32::NAN,
    };
    single_output(outputs, Value::Float(result))
}

