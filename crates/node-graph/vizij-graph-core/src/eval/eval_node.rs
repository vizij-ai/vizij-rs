/// Evaluates input node.
fn eval_input_node(
    rt: &GraphRuntime,
    spec: &NodeSpec,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let params = &spec.params;
    let path = params
        .path
        .as_ref()
        .ok_or_else(|| format!("Input node '{}' missing required 'path' parameter", spec.id))?;
    let path_str = path.to_string();
    let declared_shape = spec.output_shapes.get("out");
    let staged = rt.get_input(path).cloned();

    if let Some(target_shape) = declared_shape {
        if let Some(staged_input) = staged {
            let port = align_input_to_declared(
                &spec.id,
                &path_str,
                "staged input",
                target_shape,
                staged_input.declared.as_ref(),
                staged_input.value,
            )?;
            return keyed_port(outputs, "out", port);
        }

        if let Some(default_value) = params.value.clone() {
            let port = align_input_to_declared(
                &spec.id,
                &path_str,
                "default value",
                target_shape,
                None,
                default_value,
            )?;
            return keyed_port(outputs, "out", port);
        }

        if is_numeric_like(&target_shape.id) {
            return keyed_port(
                outputs,
                "out",
                PortValue::with_shape(
                    null_of_shape_numeric(&target_shape.id),
                    target_shape.clone(),
                ),
            );
        }

        return Err(format!(
            "Input node '{}' missing staged value for '{}' and declared non-numeric shape {:?}",
            spec.id, path_str, target_shape.id
        ));
    }

    if let Some(StagedInput {
        value, declared, ..
    }) = staged
    {
        let mut port = PortValue::new(value);
        if let Some(shape) = declared {
            port.set_shape(shape);
        }
        return keyed_port(outputs, "out", port);
    }

    if let Some(default_value) = params.value.clone() {
        return keyed_output(outputs, "out", default_value);
    }

    Err(format!(
        "Input node '{}' missing staged value for '{}' and no default provided",
        spec.id, path_str
    ))
}

/// Internal helper for `align_input_to_declared`.
fn align_input_to_declared(
    node_id: &str,
    path: &str,
    source: &str,
    declared: &Shape,
    staged_shape: Option<&Shape>,
    value: Value,
) -> Result<PortValue, String> {
    if let Some(shape) = staged_shape {
        if shape.id != declared.id && !is_numeric_like(&declared.id) {
            return Err(format!(
                "Input node '{}' received {} for '{}' with incompatible shape {:?} (expected {:?})",
                node_id, source, path, shape.id, declared.id
            ));
        }
    }

    if value_matches_shape(&declared.id, &value) {
        return Ok(PortValue::with_shape(value, declared.clone()));
    }

    if !is_numeric_like(&declared.id) {
        return Err(format!(
            "Input node '{}' cannot coerce {} for '{}' into declared shape {:?}",
            node_id, source, path, declared.id
        ));
    }

    if let Some(coerced) = coerce_numeric_to_shape(&declared.id, &value) {
        return Ok(PortValue::with_shape(coerced, declared.clone()));
    }

    Ok(PortValue::with_shape(
        null_of_shape_numeric(&declared.id),
        declared.clone(),
    ))
}

/// Internal helper for `enforce_output_shapes_slots`.
fn enforce_output_shapes_slots(
    spec: &NodeSpec,
    layout: &PortLayout,
    outputs: &mut [PortValue],
) -> Result<(), String> {
    if spec.output_shapes.is_empty() {
        return Ok(());
    }

    for (key, declared) in spec.output_shapes.iter() {
        let slot = layout.slot(key).ok_or_else(|| {
            format!(
                "node '{}' missing declared output '{}' during evaluation",
                spec.id, key
            )
        })?;
        let port = outputs.get_mut(slot).ok_or_else(|| {
            format!(
                "node '{}' missing declared output '{}' during evaluation",
                spec.id, key
            )
        })?;

        if !value_matches_shape(&declared.id, &port.value) {
            return Err(format!(
                "node '{}' output '{}' does not match declared shape {:?}",
                spec.id, key, declared.id
            ));
        }

        port.shape = declared.clone();
    }

    Ok(())
}

/// Materialize a legacy output map for external consumers/tests.
///
/// The map keys match the stable output port names from the layout.
/// Missing slots are omitted rather than filled with defaults.
///
/// # Examples
///
/// ```ignore
/// // Internal helper; `evaluate_all` handles output materialization.
/// ```
pub fn materialize_outputs(layout: &PortLayout, slots: &[PortValue]) -> HashMap<String, PortValue> {
    let mut map = HashMap::with_capacity(layout.slots.len());
    for (idx, name) in layout.slots.iter().enumerate() {
        if let Some(port) = slots.get(idx) {
            map.insert(name.clone(), port.clone());
        }
    }
    map
}

/// Gather the most recent outputs for each node input, applying selectors.
///
/// The returned `present` flags indicate which inputs were explicitly connected or defaulted.
/// Inputs without a connection or default are filled with a `0.0` scalar value.
///
/// # Examples
///
/// ```ignore
/// // Internal helper; the evaluator calls this when walking nodes.
/// ```
///
/// # Errors
///
/// Returns an error if the plan cache is missing bindings/layouts or selector projection fails.
pub fn read_inputs(
    rt: &GraphRuntime,
    node_idx: usize,
    plan: &PlanCache,
) -> Result<(Vec<PortValue>, Vec<bool>), String> {
    let layout = plan
        .layouts
        .get(node_idx)
        .ok_or_else(|| format!("missing layout for node index {}", node_idx))?;
    let bindings = plan
        .input_bindings
        .get(node_idx)
        .ok_or_else(|| format!("missing input bindings for node index {}", node_idx))?;

    let mut resolved = vec![PortValue::default(); layout.inputs.slots.len()];
    let mut present = vec![false; layout.inputs.slots.len()];

    for (slot_idx, binding) in bindings.iter().enumerate() {
        let mut port = if let Some(src) = &binding.source {
            let base = rt
                .outputs_vec
                .get(src.node_idx)
                .and_then(|vec| vec.get(src.slot))
                .cloned();
            base.or_else(|| binding.default.clone())
                .unwrap_or_else(|| PortValue::new(Value::Float(0.0)))
        } else {
            binding
                .default
                .clone()
                .unwrap_or_else(|| PortValue::new(Value::Float(0.0)))
        };

        let has_value = binding.source.is_some() || binding.default.is_some();

        if let Some(src) = &binding.source {
            if let Some(selector) = &src.selector {
                let source_label = plan
                    .layouts
                    .get(src.node_idx)
                    .and_then(|l| l.outputs.slot_name(src.slot))
                    .unwrap_or("<slot>");
                let source_port_id = src.output_name.as_str();
                let input_label = layout.inputs.slot_name(slot_idx).unwrap_or("<slot>");
                let (value, shape_id) = project_by_selector(
                    &port.value,
                    Some(&port.shape.id),
                    selector,
                )
                .map_err(|err| {
                    format!(
                        "selector {:?} on edge [node_idx {}] {}:{} -> {} failed: {}",
                        selector, src.node_idx, source_label, source_port_id, input_label, err
                    )
                })?;
                port = match shape_id {
                    Some(id) => PortValue::with_shape(value, Shape::new(id)),
                    None => PortValue::new(value),
                };
            }
        }

        if let Some(slot) = resolved.get_mut(slot_idx) {
            *slot = port;
        }
        if let Some(slot_present) = present.get_mut(slot_idx) {
            *slot_present = has_value;
        }
    }

    Ok((resolved, present))
}
