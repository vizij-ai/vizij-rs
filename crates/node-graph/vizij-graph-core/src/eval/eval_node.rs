/// Write to a named output slot.
fn keyed_output(outputs: &mut OutputSlots, key: &str, value: Value) -> Result<(), String> {
    outputs.set_value(key, value)
}

/// Write a pre-shaped port value to a named output slot.
fn keyed_port(outputs: &mut OutputSlots, key: &str, port: PortValue) -> Result<(), String> {
    outputs.set_port(key, port)
}

/// Write to the default `out` slot.
fn single_output(outputs: &mut OutputSlots, value: Value) -> Result<(), String> {
    keyed_output(outputs, "out", value)
}

/// Evaluate a single node, updating `rt` with new outputs and queued writes.
///
/// This function performs shape enforcement and appends `WriteOp`s for `Output` nodes.
///
/// # Errors
///
/// Returns an error if evaluation or shape enforcement fails.
pub fn eval_node(
    rt: &mut GraphRuntime,
    spec: &NodeSpec,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    evaluate_kind(rt, spec, inputs, outputs)?;
    enforce_output_shapes_slots(spec, outputs.layout, outputs.as_mut_slice())?;

    // Only explicit sink nodes (Output) publish external writes.
    if matches!(spec.kind, NodeType::Output) {
        if let Some(path) = spec.params.path.clone() {
            if let Some(slot) = outputs.layout.slot("out") {
                if let Some(port) = outputs.as_slice().get(slot) {
                    rt.writes.push(WriteOp::new_with_shape(
                        path,
                        port.value.clone(),
                        Some(port.shape.clone()),
                    ));
                }
            }
        }
    }
    Ok(())
}

/// Select the evaluation path for the node kind and run it.
///
/// # Errors
///
/// Returns an error if the node cannot be evaluated with the provided inputs.
fn evaluate_kind(
    rt: &mut GraphRuntime,
    spec: &NodeSpec,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let params = &spec.params;
    match &spec.kind {
        NodeType::Constant => eval_constant(params, outputs),
        NodeType::Slider => eval_slider(params, outputs),
        NodeType::MultiSlider => eval_multi_slider(params, outputs),
        node_type @ (NodeType::Add
        | NodeType::Subtract
        | NodeType::Multiply
        | NodeType::Divide
        | NodeType::Power
        | NodeType::Log
        | NodeType::Modulo) => eval_arithmetic(node_type, inputs, outputs),
        node_type @ (NodeType::Sin | NodeType::Cos | NodeType::Tan) => {
            eval_trig(node_type, inputs, outputs)
        }
        node_type @ (NodeType::Abs | NodeType::Sqrt | NodeType::Sign) => {
            eval_unary_scalar(node_type, inputs, outputs)
        }
        node_type @ (NodeType::Min | NodeType::Max) => eval_min_max(node_type, inputs, outputs),
        NodeType::Round => eval_round(params, inputs, outputs),
        NodeType::Time => eval_time(rt, outputs),
        NodeType::Oscillator => eval_oscillator(rt, inputs, outputs),
        node_type @ (NodeType::Spring | NodeType::Damp | NodeType::Slew) => {
            eval_stateful(node_type, rt, spec, params, inputs, outputs)
        }
        node_type @ (NodeType::And | NodeType::Or | NodeType::Not | NodeType::Xor) => {
            eval_logic(node_type, inputs, outputs)
        }
        node_type @ (NodeType::GreaterThan
        | NodeType::LessThan
        | NodeType::Equal
        | NodeType::NotEqual) => eval_comparison(node_type, inputs, outputs),
        NodeType::If => eval_if(inputs, outputs),
        NodeType::Clamp => eval_clamp(inputs, outputs),
        NodeType::Remap => eval_remap(inputs, outputs),
        NodeType::CenteredRemap => eval_centered_remap(inputs, outputs),
        NodeType::PiecewiseRemap => eval_piecewise_remap(params, inputs, outputs),
        NodeType::Vec3Cross => eval_vec3_cross(inputs, outputs),
        NodeType::VectorConstant => eval_vector_constant(params, outputs),
        node_type @ (NodeType::VectorAdd
        | NodeType::VectorSubtract
        | NodeType::VectorMultiply
        | NodeType::VectorScale) => eval_vector_arithmetic(node_type, inputs, outputs),
        NodeType::VectorNormalize => eval_vector_normalize(inputs, outputs),
        NodeType::VectorDot => eval_vector_dot(inputs, outputs),
        NodeType::VectorLength => eval_vector_length(inputs, outputs),
        NodeType::VectorIndex => eval_vector_index(inputs, outputs),
        NodeType::Join => eval_join(inputs, outputs),
        NodeType::Split => eval_split(params, inputs, outputs),
        node_type @ (NodeType::VectorMin
        | NodeType::VectorMax
        | NodeType::VectorMean
        | NodeType::VectorMedian
        | NodeType::VectorMode) => eval_vector_reducer(node_type, inputs, outputs),
        NodeType::WeightedSumVector => eval_weighted_sum_vector(inputs, outputs),
        NodeType::DefaultBlend => eval_default_blend(inputs, outputs),
        NodeType::BlendWeightedAverage => eval_blend_weighted_average(inputs, outputs),
        NodeType::BlendAdditive => eval_blend_additive(inputs, outputs),
        NodeType::BlendMultiply => eval_blend_multiply(inputs, outputs),
        NodeType::BlendWeightedOverlay => eval_blend_weighted_overlay(inputs, outputs),
        NodeType::BlendWeightedAverageOverlay => {
            eval_blend_weighted_average_overlay(inputs, outputs)
        }
        NodeType::BlendMax => eval_blend_max(inputs, outputs),
        NodeType::InverseKinematics => eval_inverse_kinematics(inputs, outputs),
        #[cfg(feature = "urdf_ik")]
        NodeType::UrdfIkPosition => eval_urdf_position(rt, spec, params, inputs, outputs),
        #[cfg(not(feature = "urdf_ik"))]
        NodeType::UrdfIkPosition => {
            Err("UrdfIkPosition node requires the 'urdf_ik' feature".to_string())
        }
        #[cfg(feature = "urdf_ik")]
        NodeType::UrdfIkPose => eval_urdf_pose(rt, spec, params, inputs, outputs),
        #[cfg(not(feature = "urdf_ik"))]
        NodeType::UrdfIkPose => Err("UrdfIkPose node requires the 'urdf_ik' feature".to_string()),
        #[cfg(feature = "urdf_ik")]
        NodeType::UrdfFk => eval_urdf_fk(rt, spec, params, inputs, outputs),
        #[cfg(not(feature = "urdf_ik"))]
        NodeType::UrdfFk => Err("UrdfFk node requires the 'urdf_ik' feature".to_string()),
        NodeType::Case => eval_case(params, inputs, outputs),
        NodeType::Input => eval_input_node(rt, spec, outputs),
        NodeType::Output => eval_output(inputs, outputs),
    }
}

/// Internal helper for `input_or_default`.
fn input_or_default(inputs: &InputSlots, key: &str) -> PortValue {
    inputs
        .get(key)
        .cloned()
        .unwrap_or_else(|| PortValue::new(Value::Float(0.0)))
}

/// Evaluates constant.
fn eval_constant(params: &NodeParams, outputs: &mut OutputSlots) -> Result<(), String> {
    single_output(outputs, params.value.clone().unwrap_or(Value::Float(0.0)))
}

/// Evaluates slider.
fn eval_slider(params: &NodeParams, outputs: &mut OutputSlots) -> Result<(), String> {
    single_output(
        outputs,
        Value::Float(params.value.as_ref().map(as_float).unwrap_or(0.0)),
    )
}

/// Evaluates multi slider.
fn eval_multi_slider(params: &NodeParams, outputs: &mut OutputSlots) -> Result<(), String> {
    keyed_output(outputs, "x", Value::Float(params.x.unwrap_or(0.0)))?;
    keyed_output(outputs, "y", Value::Float(params.y.unwrap_or(0.0)))?;
    keyed_output(outputs, "z", Value::Float(params.z.unwrap_or(0.0)))
}

/// Evaluates arithmetic.
fn eval_arithmetic(
    kind: &NodeType,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    match kind {
        NodeType::Add => {
            let values: Vec<Value> = collect_operand_ports(inputs)
                .iter()
                .map(|pv| pv.value.clone())
                .collect();
            let result = fold_numeric_variadic(&values, |x, y| x + y, Value::Float(0.0));
            single_output(outputs, result)
        }
        NodeType::Multiply => {
            let values: Vec<Value> = collect_operand_ports(inputs)
                .iter()
                .map(|pv| pv.value.clone())
                .collect();
            let result = fold_numeric_variadic(&values, |x, y| x * y, Value::Float(1.0));
            single_output(outputs, result)
        }
        NodeType::Subtract => {
            let lhs = input_or_default(inputs, "lhs");
            let rhs = input_or_default(inputs, "rhs");
            single_output(
                outputs,
                binary_numeric(&lhs.value, &rhs.value, |x, y| x - y),
            )
        }
        NodeType::Divide => {
            let lhs = input_or_default(inputs, "lhs");
            let rhs = input_or_default(inputs, "rhs");
            single_output(
                outputs,
                binary_numeric(&lhs.value, &rhs.value, |x, y| {
                    if y.abs() <= f32::EPSILON {
                        f32::NAN
                    } else {
                        x / y
                    }
                }),
            )
        }
        NodeType::Power => {
            let base = input_or_default(inputs, "base");
            let exp = input_or_default(inputs, "exp");
            single_output(
                outputs,
                binary_numeric(&base.value, &exp.value, |x, y| x.powf(y)),
            )
        }
        NodeType::Log => {
            let value = input_or_default(inputs, "value");
            let base = input_or_default(inputs, "base");
            single_output(
                outputs,
                binary_numeric(&value.value, &base.value, |x, b| x.log(b)),
            )
        }
        NodeType::Modulo => {
            let lhs = input_or_default(inputs, "lhs");
            let rhs = input_or_default(inputs, "rhs");
            single_output(
                outputs,
                binary_numeric(&lhs.value, &rhs.value, |x, y| {
                    if y.abs() <= f32::EPSILON {
                        f32::NAN
                    } else {
                        x % y
                    }
                }),
            )
        }
        _ => Err(format!("unsupported arithmetic node {:?}", kind)),
    }
}

/// Evaluates unary scalar.
fn eval_unary_scalar(
    kind: &NodeType,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let input = input_or_default(inputs, "in");
    match kind {
        NodeType::Abs => single_output(outputs, unary_numeric(&input.value, |x| x.abs())),
        NodeType::Sqrt => single_output(outputs, unary_numeric(&input.value, |x| x.sqrt())),
        NodeType::Sign => single_output(
            outputs,
            unary_numeric(&input.value, |x| {
                if x > 0.0 {
                    1.0
                } else if x < 0.0 {
                    -1.0
                } else {
                    0.0
                }
            }),
        ),
        _ => Err(format!("unsupported unary scalar node {:?}", kind)),
    }
}

/// Evaluates min max.
fn eval_min_max(
    kind: &NodeType,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let values: Vec<Value> = collect_operand_ports(inputs)
        .iter()
        .map(|pv| pv.value.clone())
        .collect();
    let empty = Value::Float(f32::NAN);
    let op = match kind {
        NodeType::Min => f32::min,
        NodeType::Max => f32::max,
        _ => unreachable!(),
    };
    let result = fold_numeric_variadic(&values, op, empty);
    single_output(outputs, result)
}

/// Evaluates round.
fn eval_round(
    params: &NodeParams,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let input = input_or_default(inputs, "in");
    let mode = params.round_mode.clone().unwrap_or_default();
    let op: fn(f32) -> f32 = match mode {
        RoundMode::Floor => f32::floor,
        RoundMode::Ceil => f32::ceil,
        RoundMode::Trunc => f32::trunc,
    };
    single_output(outputs, unary_numeric(&input.value, op))
}

/// Evaluates trig.
fn eval_trig(
    kind: &NodeType,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let input = input_or_default(inputs, "in");
    let op = match kind {
        NodeType::Sin => f32::sin,
        NodeType::Cos => f32::cos,
        NodeType::Tan => f32::tan,
        _ => unreachable!(),
    };
    single_output(outputs, unary_numeric(&input.value, op))
}

/// Evaluates time.
fn eval_time(rt: &GraphRuntime, outputs: &mut OutputSlots) -> Result<(), String> {
    single_output(outputs, Value::Float(rt.t))
}

/// Evaluates oscillator.
fn eval_oscillator(
    rt: &GraphRuntime,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let freq_port = input_or_default(inputs, "frequency");
    let phase_port = input_or_default(inputs, "phase");

    let freq_value = freq_port.value.clone();
    let phase_value = phase_port.value.clone();

    let freq_flat = flatten_numeric(&freq_value);
    let phase_flat = flatten_numeric(&phase_value);

    let value = match (freq_flat, phase_flat) {
        (Some(freq_flat), Some(phase_flat)) => match align_flattened(&freq_flat, &phase_flat) {
            Ok((layout, freqs, phases)) => {
                let data: Vec<f32> = freqs
                    .into_iter()
                    .zip(phases)
                    .map(|(f, phase)| (std::f32::consts::TAU * f * rt.t + phase).sin())
                    .collect();
                layout.reconstruct(&data)
            }
            Err(layout) => layout.fill_with(f32::NAN),
        },
        (Some(freq_flat), None) => {
            let FlatValue {
                layout,
                data: freqs,
            } = freq_flat;
            let phase_scalar = as_float(&phase_value);
            let data: Vec<f32> = freqs
                .into_iter()
                .map(|f| (std::f32::consts::TAU * f * rt.t + phase_scalar).sin())
                .collect();
            layout.reconstruct(&data)
        }
        (None, Some(phase_flat)) => {
            let FlatValue {
                layout,
                data: phases,
            } = phase_flat;
            let freq_scalar = as_float(&freq_value);
            let data: Vec<f32> = phases
                .into_iter()
                .map(|phase| (std::f32::consts::TAU * freq_scalar * rt.t + phase).sin())
                .collect();
            layout.reconstruct(&data)
        }
        (None, None) => {
            let f = as_float(&freq_value);
            let phase = as_float(&phase_value);
            Value::Float((std::f32::consts::TAU * f * rt.t + phase).sin())
        }
    };

    single_output(outputs, value)
}
/// Evaluates stateful.
fn eval_stateful(
    kind: &NodeType,
    rt: &mut GraphRuntime,
    spec: &NodeSpec,
    params: &NodeParams,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let input = input_or_default(inputs, "in");
    match (kind, flatten_numeric(&input.value)) {
        (NodeType::Spring, Some(flat)) => {
            let dt = if rt.dt.is_finite() {
                rt.dt.max(0.0)
            } else {
                0.0
            };
            let stiffness = params.stiffness.unwrap_or(120.0);
            let stiffness = if stiffness.is_finite() {
                stiffness.max(0.0)
            } else {
                0.0
            };
            let damping = params.damping.unwrap_or(20.0);
            let damping = if damping.is_finite() {
                damping.max(0.0)
            } else {
                0.0
            };
            let mass = params.mass.unwrap_or(1.0);
            const MIN_MASS: f32 = 1.0e-4;
            let mass = if mass.is_finite() {
                mass.max(MIN_MASS)
            } else {
                1.0
            };

            let state = rt.spring_state_mut(&spec.id, &flat);
            state.target = flat.data.clone();

            if dt <= 0.0 {
                state.position = state.target.clone();
                state.velocity.fill(0.0);
            } else {
                let inv_mass = 1.0 / mass;
                for ((pos, vel), target) in state
                    .position
                    .iter_mut()
                    .zip(state.velocity.iter_mut())
                    .zip(state.target.iter())
                {
                    let displacement = *pos - *target;
                    let spring_force = -stiffness * displacement;
                    let damping_force = -damping * *vel;
                    let acceleration = (spring_force + damping_force) * inv_mass;
                    *vel += acceleration * dt;
                    *pos += *vel * dt;
                }
            }

            single_output(outputs, state.layout.reconstruct(&state.position))
        }
        (NodeType::Damp, Some(flat)) => {
            let dt = if rt.dt.is_finite() {
                rt.dt.max(0.0)
            } else {
                0.0
            };
            let half_life = params.half_life.unwrap_or(0.1);
            let half_life = if half_life.is_finite() {
                half_life
            } else {
                0.1
            };
            let state = rt.damp_state_mut(&spec.id, &flat);
            if dt <= 0.0 || half_life <= 0.0 {
                state.value = flat.data.clone();
            } else {
                let hl = half_life.max(1.0e-6);
                let decay = (-std::f32::consts::LN_2 * dt / hl).exp();
                for (value, target) in state.value.iter_mut().zip(flat.data.iter()) {
                    *value = *target + (*value - *target) * decay;
                }
            }
            single_output(outputs, state.layout.reconstruct(&state.value))
        }
        (NodeType::Slew, Some(flat)) => {
            let dt = if rt.dt.is_finite() {
                rt.dt.max(0.0)
            } else {
                0.0
            };
            let max_rate = params.max_rate.unwrap_or(1.0);
            let max_rate = if max_rate.is_finite() { max_rate } else { 1.0 };
            let state = rt.slew_state_mut(&spec.id, &flat);
            if dt <= 0.0 || max_rate <= 0.0 {
                state.value = flat.data.clone();
            } else {
                let max_delta = max_rate * dt;
                for (value, target) in state.value.iter_mut().zip(flat.data.iter()) {
                    let delta = *target - *value;
                    if delta.abs() <= max_delta {
                        *value = *target;
                    } else if delta > 0.0 {
                        *value += max_delta;
                    } else {
                        *value -= max_delta;
                    }
                }
            }
            single_output(outputs, state.layout.reconstruct(&state.value))
        }
        _ => single_output(outputs, Value::Float(f32::NAN)),
    }
}

/// Evaluates logic.
fn eval_logic(
    kind: &NodeType,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let value = match kind {
        NodeType::And => {
            as_bool(&input_or_default(inputs, "lhs").value)
                && as_bool(&input_or_default(inputs, "rhs").value)
        }
        NodeType::Or => {
            as_bool(&input_or_default(inputs, "lhs").value)
                || as_bool(&input_or_default(inputs, "rhs").value)
        }
        NodeType::Xor => {
            as_bool(&input_or_default(inputs, "lhs").value)
                ^ as_bool(&input_or_default(inputs, "rhs").value)
        }
        NodeType::Not => !as_bool(&input_or_default(inputs, "in").value),
        _ => unreachable!(),
    };
    single_output(outputs, Value::Bool(value))
}

/// Evaluates comparison.
fn eval_comparison(
    kind: &NodeType,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let lhs = input_or_default(inputs, "lhs");
    let rhs = input_or_default(inputs, "rhs");
    let value = match kind {
        NodeType::GreaterThan => as_float(&lhs.value) > as_float(&rhs.value),
        NodeType::LessThan => as_float(&lhs.value) < as_float(&rhs.value),
        NodeType::Equal => (as_float(&lhs.value) - as_float(&rhs.value)).abs() < 1e-6,
        NodeType::NotEqual => (as_float(&lhs.value) - as_float(&rhs.value)).abs() > 1e-6,
        _ => unreachable!(),
    };
    single_output(outputs, Value::Bool(value))
}

/// Evaluates if.
fn eval_if(inputs: &InputSlots, outputs: &mut OutputSlots) -> Result<(), String> {
    let cond = as_bool(&input_or_default(inputs, "cond").value);
    let branch = if cond {
        input_or_default(inputs, "then")
    } else {
        input_or_default(inputs, "else")
    };
    single_output(outputs, branch.value)
}

/// Evaluates clamp.
fn eval_clamp(inputs: &InputSlots, outputs: &mut OutputSlots) -> Result<(), String> {
    let value = inputs
        .get("in")
        .ok_or_else(|| "Clamp requires 'in' input".to_string())?;
    let min = inputs
        .get("min")
        .ok_or_else(|| "Clamp requires 'min' input".to_string())?;
    let max = inputs
        .get("max")
        .ok_or_else(|| "Clamp requires 'max' input".to_string())?;

    let clamped_low = binary_numeric(&value.value, &min.value, |x, m| x.max(m));
    single_output(
        outputs,
        binary_numeric(&clamped_low, &max.value, |x, m| x.min(m)),
    )
}

/// Evaluates remap.
fn eval_remap(inputs: &InputSlots, outputs: &mut OutputSlots) -> Result<(), String> {
    let value = inputs
        .get("in")
        .ok_or_else(|| "Remap requires 'in' input".to_string())?;
    let in_min = inputs
        .get("in_min")
        .ok_or_else(|| "Remap requires 'in_min' input".to_string())?;
    let in_max = inputs
        .get("in_max")
        .ok_or_else(|| "Remap requires 'in_max' input".to_string())?;
    let out_min = inputs
        .get("out_min")
        .ok_or_else(|| "Remap requires 'out_min' input".to_string())?;
    let out_max = inputs
        .get("out_max")
        .ok_or_else(|| "Remap requires 'out_max' input".to_string())?;

    let numer = binary_numeric(&value.value, &in_min.value, |v, min| v - min);
    let denom = binary_numeric(&in_max.value, &in_min.value, |max, min| max - min);
    let ratio = binary_numeric(
        &numer,
        &denom,
        |n, d| if d != 0.0 { n / d } else { f32::NAN },
    );
    let ratio_clamped = unary_numeric(&ratio, |x| x.clamp(0.0, 1.0));
    let span = binary_numeric(&out_max.value, &out_min.value, |max, min| max - min);
    let scaled = binary_numeric(&ratio_clamped, &span, |t, span| t * span);
    single_output(
        outputs,
        binary_numeric(&scaled, &out_min.value, |scaled, min| scaled + min),
    )
}

/// Evaluates centered remap.
fn eval_centered_remap(inputs: &InputSlots, outputs: &mut OutputSlots) -> Result<(), String> {
    let value = inputs
        .get("in")
        .ok_or_else(|| "CenteredRemap requires 'in' input".to_string())?;
    let in_low = inputs
        .get("in_low")
        .ok_or_else(|| "CenteredRemap requires 'in_low' input".to_string())?;
    let in_anchor = inputs
        .get("in_anchor")
        .ok_or_else(|| "CenteredRemap requires 'in_anchor' input".to_string())?;
    let in_high = inputs
        .get("in_high")
        .ok_or_else(|| "CenteredRemap requires 'in_high' input".to_string())?;
    let out_low = inputs
        .get("out_low")
        .ok_or_else(|| "CenteredRemap requires 'out_low' input".to_string())?;
    let out_anchor = inputs
        .get("out_anchor")
        .ok_or_else(|| "CenteredRemap requires 'out_anchor' input".to_string())?;
    let out_high = inputs
        .get("out_high")
        .ok_or_else(|| "CenteredRemap requires 'out_high' input".to_string())?;

    let in_low = as_float(&in_low.value);
    let in_anchor = as_float(&in_anchor.value);
    let in_high = as_float(&in_high.value);
    let out_low = as_float(&out_low.value);
    let out_anchor = as_float(&out_anchor.value);
    let out_high = as_float(&out_high.value);

    let below_span = in_anchor - in_low;
    let above_span = in_high - in_anchor;
    let below_slope = if below_span.abs() > f32::EPSILON {
        (out_anchor - out_low) / below_span
    } else {
        0.0
    };
    let above_slope = if above_span.abs() > f32::EPSILON {
        (out_high - out_anchor) / above_span
    } else {
        0.0
    };

    let remapped = unary_numeric(&value.value, |x| {
        if x <= in_anchor {
            out_anchor + below_slope * (x - in_anchor)
        } else {
            out_anchor + above_slope * (x - in_anchor)
        }
    });
    single_output(outputs, remapped)
}

/// Evaluates piecewise remap.
fn eval_piecewise_remap(
    params: &NodeParams,
    inputs: &InputSlots,
    outputs: &mut OutputSlots,
) -> Result<(), String> {
    let value_port = inputs
        .get("in")
        .ok_or_else(|| "PiecewiseRemap requires 'in' input".to_string())?;
    let input_breakpoints_port = inputs
        .get("input_breakpoints")
        .ok_or_else(|| "PiecewiseRemap requires 'input_breakpoints' input".to_string())?;
    let output_breakpoints_port = inputs
        .get("output_breakpoints")
        .ok_or_else(|| "PiecewiseRemap requires 'output_breakpoints' input".to_string())?;

    let input_flat = flatten_numeric(&input_breakpoints_port.value)
        .ok_or_else(|| "PiecewiseRemap expects numeric input breakpoints".to_string())?;
    let output_flat = flatten_numeric(&output_breakpoints_port.value)
        .ok_or_else(|| "PiecewiseRemap expects numeric output breakpoints".to_string())?;

    let config = prepare_piecewise_breakpoints(input_flat.data, output_flat.data)?;
    let clamp_enabled = params.clamp.unwrap_or(false);

    let remapped = match flatten_numeric(&value_port.value) {
        Some(flat) => {
            let data: Vec<f32> = flat
                .data
                .iter()
                .map(|component| {
                    remap_piecewise_scalar(
                        *component,
                        &config.inputs,
                        &config.outputs,
                        clamp_enabled,
                        config.constant,
                    )
                })
                .collect();
            flat.layout.reconstruct(&data)
        }
        None => Value::Float(f32::NAN),
    };

    single_output(outputs, remapped)
}

struct PiecewiseConfig {
    inputs: Vec<f32>,
    outputs: Vec<f32>,
    constant: Option<f32>,
}

/// Prepares piecewise breakpoints.
fn prepare_piecewise_breakpoints(
    inputs: Vec<f32>,
    outputs: Vec<f32>,
) -> Result<PiecewiseConfig, String> {
    if inputs.len() != outputs.len() {
        return Err(
            "PiecewiseRemap requires equal numbers of input and output breakpoints".to_string(),
        );
    }
    if inputs.len() < 2 {
        return Err("PiecewiseRemap requires at least two breakpoints".to_string());
    }

    for (idx, value) in inputs.iter().enumerate() {
        if !value.is_finite() {
            return Err(format!(
                "PiecewiseRemap input_breakpoints[{idx}] must be finite"
            ));
        }
    }
    for (idx, value) in outputs.iter().enumerate() {
        if !value.is_finite() {
            return Err(format!(
                "PiecewiseRemap output_breakpoints[{idx}] must be finite"
            ));
        }
    }

    let mut unique_inputs = Vec::with_capacity(inputs.len());
    let mut unique_outputs = Vec::with_capacity(outputs.len());

    for (idx, (input, output)) in inputs.into_iter().zip(outputs.into_iter()).enumerate() {
        if let Some(&prev_input) = unique_inputs.last() {
            let delta: f32 = input - prev_input;
            if delta < -PIECEWISE_BREAKPOINT_EPS {
                return Err(format!(
                    "PiecewiseRemap requires non-decreasing input breakpoints; index {idx} violates ordering"
                ));
            }
            if delta.abs() <= PIECEWISE_BREAKPOINT_EPS {
                // Only coalesce duplicates when both input and output are effectively identical.
                if let Some(&prev_output) = unique_outputs.last() {
                    let output_delta: f32 = output - prev_output;
                    if output_delta.abs() <= PIECEWISE_OUTPUT_EPS {
                        continue;
                    }
                }
            }
        }
        unique_inputs.push(input);
        unique_outputs.push(output);
    }

    if unique_inputs.len() == 1 {
        let constant = unique_outputs.first().copied();
        return Ok(PiecewiseConfig {
            inputs: unique_inputs,
            outputs: unique_outputs,
            constant,
        });
    }

    Ok(PiecewiseConfig {
        inputs: unique_inputs,
        outputs: unique_outputs,
        constant: None,
    })
}

/// Internal helper for `remap_piecewise_scalar`.
fn remap_piecewise_scalar(
    value: f32,
    inputs: &[f32],
    outputs: &[f32],
    clamp: bool,
    constant: Option<f32>,
) -> f32 {
    if !value.is_finite() {
        return f32::NAN;
    }
    if let Some(constant_value) = constant {
        return constant_value;
    }

    debug_assert!(
        inputs.len() >= 2 && inputs.len() == outputs.len(),
        "prepare_piecewise_breakpoints should ensure at least two segments"
    );

    if approx_equal(value, inputs[0]) {
        return outputs[0];
    }
    if approx_equal(value, *inputs.last().unwrap()) {
        return *outputs.last().unwrap();
    }

    if value < inputs[0] {
        if clamp {
            return outputs[0];
        }
        let slope = (outputs[1] - outputs[0]) / (inputs[1] - inputs[0]);
        return outputs[0] + (value - inputs[0]) * slope;
    }

    let last_idx = inputs.len() - 1;
    if value > inputs[last_idx] {
        if clamp {
            return outputs[last_idx];
        }
        let slope =
            (outputs[last_idx] - outputs[last_idx - 1]) / (inputs[last_idx] - inputs[last_idx - 1]);
        return outputs[last_idx] + (value - inputs[last_idx]) * slope;
    }

    for idx in 1..inputs.len() {
        if approx_equal(value, inputs[idx]) {
            return outputs[idx];
        }
        if value < inputs[idx] {
            let x0 = inputs[idx - 1];
            let x1 = inputs[idx];
            let y0 = outputs[idx - 1];
            let y1 = outputs[idx];
            let span = x1 - x0;
            if span.abs() <= PIECEWISE_BREAKPOINT_EPS {
                return y1;
            }
            let t = (value - x0) / span;
            return y0 + t * (y1 - y0);
        }
    }

    outputs[last_idx]
}

/// Internal helper for `approx_equal`.
fn approx_equal(a: f32, b: f32) -> bool {
    (a - b).abs() <= PIECEWISE_BREAKPOINT_EPS
}

/// Evaluates vec3 cross.
fn eval_vec3_cross(inputs: &InputSlots, outputs: &mut OutputSlots) -> Result<(), String> {
    let a = input_or_default(inputs, "a");
    let b = input_or_default(inputs, "b");
    match (flatten_numeric(&a.value), flatten_numeric(&b.value)) {
        (Some(a_flat), Some(b_flat))
            if a_flat.layout.scalar_len() == 3 && b_flat.layout.scalar_len() == 3 =>
        {
            let ax = a_flat.data[0];
            let ay = a_flat.data[1];
            let az = a_flat.data[2];
            let bx = b_flat.data[0];
            let by = b_flat.data[1];
            let bz = b_flat.data[2];
            let result = vec![ay * bz - az * by, az * bx - ax * bz, ax * by - ay * bx];
            single_output(outputs, a_flat.layout.reconstruct(&result))
        }
        (Some(a_flat), _) => single_output(outputs, a_flat.layout.fill_with(f32::NAN)),
        (_, Some(b_flat)) => single_output(outputs, b_flat.layout.fill_with(f32::NAN)),
        _ => single_output(outputs, Value::Vec3([f32::NAN, f32::NAN, f32::NAN])),
    }
}

