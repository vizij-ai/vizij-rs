#!/usr/bin/env node

import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { spawn } from "node:child_process";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const repoRoot = resolve(__dirname, "..");
const packageRoot = resolve(repoRoot, "npm", "@vizij", "node-graph-wasm");
const metadataSourceDir = join(packageRoot, "src", "metadata");
const metadataDistDir = join(packageRoot, "dist", "src", "metadata");
const metadataSourceJsonPath = join(metadataSourceDir, "registry.json");
const metadataSourceModulePath = join(metadataSourceDir, "registry.ts");
const metadataDistJsonPath = join(metadataDistDir, "registry.json");

async function runCargoExport(outputPath) {
  await mkdir(dirname(outputPath), { recursive: true });

  const cargoArgs = [
    "run",
    "--quiet",
    "-p",
    "vizij-graph-registry-export",
    "--",
    "--output",
    outputPath,
    "--pretty",
  ];

  await new Promise((resolvePromise, rejectPromise) => {
    const child = spawn("cargo", cargoArgs, {
      cwd: repoRoot,
      stdio: "inherit",
      env: process.env,
    });

    child.on("exit", (code) => {
      if (code === 0) {
        resolvePromise();
      } else {
        rejectPromise(new Error(`cargo run exited with code ${code}`));
      }
    });
    child.on("error", rejectPromise);
  });
}

function formatRegistryModule(rawJson) {
  const trimmed = rawJson.trimEnd();
  return `/* This file is auto-generated by scripts/generate-node-registry.mjs */
import type { Registry } from "../types.js";

const registry: Registry = ${trimmed};

export default registry;
`;
}

async function writeRegistryModule(rawJson) {
  await mkdir(dirname(metadataSourceModulePath), { recursive: true });
  const contents = formatRegistryModule(rawJson);
  // Ensure trailing newline for POSIX editors
  const withNewline = contents.endsWith("\n") ? contents : `${contents}\n`;
  await writeFile(metadataSourceModulePath, withNewline, "utf8");
}

async function copyJsonToDist(rawJson) {
  await mkdir(metadataDistDir, { recursive: true });
  await writeFile(metadataDistJsonPath, rawJson, "utf8");
}

async function main() {
  await runCargoExport(metadataSourceJsonPath);
  const raw = await readFile(metadataSourceJsonPath, "utf8");
  await writeRegistryModule(raw);
  await copyJsonToDist(raw);
  console.log(
    `[generate-node-registry] wrote metadata JSON to ${metadataSourceJsonPath} and ${metadataDistJsonPath}, and baked module ${metadataSourceModulePath}`,
  );
}

main().catch((error) => {
  console.error("[generate-node-registry] Failed to generate registry:", error);
  process.exit(1);
});
