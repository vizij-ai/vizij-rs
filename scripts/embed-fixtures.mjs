#!/usr/bin/env node

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");

const fixturesRoot = path.resolve(repoRoot, "fixtures");
const manifestPath = path.join(fixturesRoot, "manifest.json");

const packages = process.argv.slice(2);

if (packages.length === 0) {
  console.error("Usage: node scripts/embed-fixtures.mjs <package> [package...]");
  console.error("Example: node scripts/embed-fixtures.mjs animation-wasm node-graph-wasm");
  process.exit(1);
}

async function loadManifest() {
  const manifestRaw = await fs.readFile(manifestPath, "utf8");
  return JSON.parse(manifestRaw);
}

function normalizeRelPath(relPath) {
  return relPath.replace(/^[.][/\\]/, "").replace(/\\/g, "/");
}

async function collectFiles(manifest) {
  const paths = new Set();

  for (const rel of Object.values(manifest.animations ?? {})) {
    if (rel) paths.add(rel);
  }

  for (const entry of Object.values(manifest["node-graphs"] ?? {})) {
    if (entry?.spec) paths.add(entry.spec);
    if (entry?.stage) paths.add(entry.stage);
  }

  for (const entry of Object.values(manifest.orchestrations ?? {})) {
    if (!entry) continue;
    if (typeof entry === "string") {
      paths.add(entry);
    } else if (typeof entry === "object" && entry.path) {
      paths.add(entry.path);
    }
  }

  const files = {};

  for (const relPath of paths) {
    const normalized = normalizeRelPath(relPath);
    const absolute = path.join(fixturesRoot, normalized);
    const raw = await fs.readFile(absolute, "utf8");
    files[normalized] = raw;
  }

  return files;
}

async function ensureGeneratedFile(packageName, payload) {
  const packageRoot = path.resolve(repoRoot, "npm", "@vizij", packageName);
  const generatedDir = path.join(packageRoot, "src", "generated");
  const outputPath = path.join(generatedDir, "fixtures-bundle.ts");

  await fs.mkdir(generatedDir, { recursive: true });

  const fileContents =
    "/* This file is auto-generated by scripts/embed-fixtures.mjs */\n" +
    "/* eslint-disable */\n" +
    "/* prettier-ignore */\n" +
    "const bundle = " +
    JSON.stringify(payload, null, 2) +
    " as const;\n\n" +
    "export default bundle;\n";

  await fs.writeFile(outputPath, fileContents, "utf8");
}

async function main() {
  const manifest = await loadManifest();
  const files = await collectFiles(manifest);

  const payload = {
    manifest,
    files,
  };

  await Promise.all(packages.map((pkg) => ensureGeneratedFile(pkg, payload)));
}

main().catch((err) => {
  console.error("[embed-fixtures] Failed to embed fixtures:", err);
  process.exit(1);
});
